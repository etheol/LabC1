#include <stdio.h>   // –≠—Ç–æ –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è –≤–≤–æ–¥–∞ –∏ –≤—ã–≤–æ–¥–∞. –ë–µ–∑ –Ω–µ—ë printf –∏ scanf –Ω–µ —Ä–∞–±–æ—Ç–∞—é—Ç.
#include <math.h>    // –≠—Ç–æ –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è –º–∞—Ç–µ–º–∞—Ç–∏–∫–∏: sin, cos, log, sqrt –∏ —Ç.–¥.

// –ï—Å–ª–∏ –≤ —Å–∏—Å—Ç–µ–º–µ –Ω–µ—Ç —á–∏—Å–ª–∞ –ü–∏ (M_PI), –æ–ø—Ä–µ–¥–µ–ª–∏–º –µ–≥–æ —Å–∞–º–∏. –≠—Ç–æ –ø—Ä–æ—Å—Ç–æ –∫–æ–Ω—Å—Ç–∞–Ω—Ç–∞ 3.14159...
#ifndef M_PI
#define M_PI 3.14159265358979323846
#endif

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤–æ–∑–≤–µ–¥–µ–Ω–∏—è —á–∏—Å–ª–∞ A –≤ 8-—é —Å—Ç–µ–ø–µ–Ω—å –∑–∞ 3 —É–º–Ω–æ–∂–µ–Ω–∏—è (–¥–ª—è –≤–∞—Ä–∏–∞–Ω—Ç–∞ 7)
double vozvedi_v_8_stepen(double A) {
    // –ú—ã –Ω–µ –±—É–¥–µ–º —É–º–Ω–æ–∂–∞—Ç—å A –Ω–∞ —Å–µ–±—è 7 —Ä–∞–∑ ‚Äî —ç—Ç–æ –¥–æ–ª–≥–æ.
    // –í–º–µ—Å—Ç–æ —ç—Ç–æ–≥–æ: —Å–Ω–∞—á–∞–ª–∞ A¬≤, –ø–æ—Ç–æ–º A‚Å¥ = A¬≤ * A¬≤, –ø–æ—Ç–æ–º A‚Å∏ = A‚Å¥ * A‚Å¥. –í—Å–µ–≥–æ 3 —É–º–Ω–æ–∂–µ–Ω–∏—è!
    double A_v_kvadrate = A * A;          // A¬≤ ‚Äî –≤–æ–∑–≤–æ–¥–∏–º –≤ –∫–≤–∞–¥—Ä–∞—Ç (1-–µ —É–º–Ω–æ–∂–µ–Ω–∏–µ)
    double A_v_4_stepeni = A_v_kvadrate * A_v_kvadrate; // A‚Å¥ ‚Äî –≤–æ–∑–≤–æ–¥–∏–º –∫–≤–∞–¥—Ä–∞—Ç –≤ –∫–≤–∞–¥—Ä–∞—Ç (2-–µ —É–º–Ω–æ–∂–µ–Ω–∏–µ)
    double A_v_8_stepeni = A_v_4_stepeni * A_v_4_stepeni; // A‚Å∏ ‚Äî –≤–æ–∑–≤–æ–¥–∏–º A‚Å¥ –≤ –∫–≤–∞–¥—Ä–∞—Ç (3-–µ —É–º–Ω–æ–∂–µ–Ω–∏–µ)
    return A_v_8_stepeni;  // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç ‚Äî —ç—Ç–æ –∏ –µ—Å—Ç—å A –≤ 8-–π —Å—Ç–µ–ø–µ–Ω–∏
}

