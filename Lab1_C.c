    scanf("%lf %lf", &a, &b);  // –°—á–∏—Ç—ã–≤–∞–µ–º –¥–≤–∞ —á–∏—Å–ª–∞

    double ploshad = a * b;                // –ü–ª–æ—â–∞–¥—å = –¥–ª–∏–Ω–∞ * —à–∏—Ä–∏–Ω–∞
    double perimetr = 2 * (a + b);         // –ü–µ—Ä–∏–º–µ—Ç—Ä = —Å—É–º–º–∞ –≤—Å–µ—Ö —Å—Ç–æ—Ä–æ–Ω * 2
    printf("–ü–ª–æ—â–∞–¥—å = %.2f\n", ploshad);
    printf("–ü–µ—Ä–∏–º–µ—Ç—Ä = %.2f\n", perimetr);

    // –ó–∞–¥–∞–Ω–∏–µ 2: –≥—Ä–∞–¥—É—Å—ã ‚Üí —Ä–∞–¥–∏–∞–Ω—ã (—Ñ–æ—Ä–º—É–ª–∞: —Ä–∞–¥–∏–∞–Ω—ã = –≥—Ä–∞–¥—É—Å—ã * œÄ / 180)
    double gradusy;
    printf("–í–≤–µ–¥–∏—Ç–µ —É–≥–æ–ª –≤ –≥—Ä–∞–¥—É—Å–∞—Ö: ");
    scanf("%lf", &gradusy);

    // M_PI ‚Äî —ç—Ç–æ –Ω–∞—à–µ —á–∏—Å–ª–æ –ü–∏ (3.14159...), –∫–æ—Ç–æ—Ä–æ–µ –º—ã –æ–ø—Ä–µ–¥–µ–ª–∏–ª–∏ –≤—ã—à–µ
    double radiany = gradusy * M_PI / 180.0;
    printf("%.2f –≥—Ä–∞–¥—É—Å–æ–≤ = %.6f —Ä–∞–¥–∏–∞–Ω\n", gradusy, radiany);

    // –ó–∞–¥–∞–Ω–∏–µ 3: –∏–∑ —Ç—Ä—ë—Ö–∑–Ω–∞—á–Ω–æ–≥–æ —á–∏—Å–ª–∞ —É–±—Ä–∞—Ç—å –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É –∏ –ø—Ä–∏–ø–∏—Å–∞—Ç—å –µ—ë –≤ –Ω–∞—á–∞–ª–æ
